public class Position2D {
    private double xPos;
    private double yPos;
    public Position2D(double x, double y){
        this.xPos = x;
        this.yPos = y;
    }
    public double getXPos(){
        return xPos;
    }
    public double getYPos(){
        return yPos;
    }
    public void setXPos(double newxPos){
        xPos = newxPos;
    }
    public void setYPos(double newyPos){
        xPos = newyPos;
    }
}
